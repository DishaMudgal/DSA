import java.util.Scanner;
		public class pattern5 {
				public static void main(String[] args) {
					Scanner s=new Scanner(System.in);
					int n=s.nextInt();
					for(int i=1;i<=n;i++) {
						for(int j=1;j<=n-i;j++) {
							System.out.print(" ");
						}
				for(int y=1;y<=(i+i)-1;y++) {
				System.out.print(i);
				}
					
				System.out.println();	 
				}
					
				
		
		for(int k=n-1;k>=1;k--) {
			for(int m=1;m<=n-k;m++) {
				System.out.print(" ");
			}
			for(int j=1;j<=(k+k)-1;j++) {
				if(k<=9&&k>=0) {
				System.out.print("0"+k);
				}
				else {
					System.out.print(k);
				}
			}
			System.out.println();	
		}
		}	
		}
		
		
		
